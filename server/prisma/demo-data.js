export function formatPhone(seed) {
  const a = 900 + (seed % 99);
  const b = 100 + ((seed * 7) % 900);
  const c = 10 + ((seed * 13) % 90);
  const d = 10 + ((seed * 3) % 90);
  return `+7 (${a}) ${b}-${c}-${d}`;
}

export function personName(index) {
  return `${pick(FIRST_NAMES, index)} ${pick(LAST_NAMES, index + 3)}`;
}
