const CYRILLIC_TO_LATIN = {
  а: "a", б: "b", в: "v", г: "g", д: "d", е: "e", ё: "e", ж: "zh", з: "z",
  и: "i", й: "y", к: "k", л: "l", м: "m", н: "n", о: "o", п: "p", р: "r",
  с: "s", т: "t", у: "u", ф: "f", х: "h", ц: "ts", ч: "ch", ш: "sh", щ: "sch",
  ъ: "", ы: "y", ь: "", э: "e", ю: "yu", я: "ya",
};

/** Детерминированный «случайный» выбор — одинаковые данные при каждом запуске. */
export function pick(list, index) {
  return list[index % list.length];
}

/** Дата N дней назад (фиксированное время для стабильности). */
export function daysAgo(days, slot = 0) {
  const d = new Date();
  d.setHours(9 + (slot % 9), (slot * 13) % 60, 0, 0);
  d.setDate(d.getDate() - days);
  return d;
}

export function transliterate(text) {
  return text
    .toLowerCase()
    .split("")
    .map((char) => CYRILLIC_TO_LATIN[char] ?? (char >= "a" && char <= "z" ? char : ""))
    .join("");
}

/** Профиль с согласованным полом имени и фамилии. */
export function personProfile(index) {
  const isFemale = index % 2 === 1;
  const firstName = pick(isFemale ? FEMALE_FIRST_NAMES : MALE_FIRST_NAMES, index);
  const lastName = pick(isFemale ? FEMALE_LAST_NAMES : MALE_LAST_NAMES, index);
  return { firstName, lastName, fullName: `${firstName} ${lastName}`, isFemale };
}

export function personName(index) {
  return personProfile(index).fullName;
}

function emailLocalPart(firstName, lastName, index) {
  const first = transliterate(firstName);
  const last = transliterate(lastName);
  const patterns = [
    `${last}.${first}`,
    `${first}.${last}`,
    `${last}${first}`,
    `${first}_${last}`,
    `${last}${(index % 90) + 10}`,
  ];
  return pick(patterns, index).replace(/[^a-z0-9._-]/g, "");
}

export function demoClientEmail(index) {
  const { firstName, lastName } = personProfile(index);
  const domain = pick(EMAIL_DOMAINS, index);
  return `${emailLocalPart(firstName, lastName, index)}@${domain}`;
}

export function contactLeadEmail(index) {
  const profile = personProfile(index + 10);
  const domain = pick(EMAIL_DOMAINS, index + 2);
  return `${emailLocalPart(profile.firstName, profile.lastName, index + 5)}@${domain}`;
}

export function formatPhone(seed) {
  const a = 900 + (seed % 99);
  const b = 100 + ((seed * 7) % 900);
  const c = 10 + ((seed * 13) % 90);
  const d = 10 + ((seed * 3) % 90);
  return `+7 (${a}) ${b}-${c}-${d}`;
}

/** Веса активности по дням: волны, всплески, затишье в выходные. */
export function activityWeights(spanDays, variant = 0) {
  return Array.from({ length: spanDays }, (_, d) => {
    const weekday = (d + 3 + variant) % 7;
    const weekendFactor = weekday >= 5 ? 0.22 : 1;
    const wave =
      1.2 +
      2.8 * Math.sin((d + variant * 2) / 5.5) +
      1.6 * Math.cos((d + variant) / 17) +
      1.1 * Math.sin((d + variant * 5) / 2.8);
    const spike = (d + variant * 11) % 29 === 0 ? 6 : (d + variant * 7) % 19 === 0 ? 4 : 0;
    const drought = (d + variant * 13) % 31 === 0 ? -1.5 : 0;
    return Math.max(0.15, (wave + spike + drought) * weekendFactor);
  });
}

/** Распределить N записей по дням согласно весам (детерминированно). */
export function distributeDayOffsets(count, spanDays, variant = 0) {
  const weights = activityWeights(spanDays, variant);
  const totalWeight = weights.reduce((a, b) => a + b, 0);
  const targets = weights.map((w) =>
    Math.max(0, Math.round((w / totalWeight) * count)),
  );
  let sum = targets.reduce((a, b) => a + b, 0);
  let cursor = 0;
  while (sum < count) {
    targets[cursor % spanDays] += 1;
    sum += 1;
    cursor += 1;
  }
  while (sum > count) {
    const idx = targets.findIndex((t) => t > 0);
    if (idx === -1) break;
    targets[idx] -= 1;
    sum -= 1;
  }
  const offsets = [];
  for (let day = 0; day < spanDays; day += 1) {
    for (let i = 0; i < targets[day]; i += 1) {
      offsets.push(day);
    }
  }
  for (let i = offsets.length - 1; i > 0; i -= 1) {
    const j = (i * 7919 + variant * 104729) % (i + 1);
    [offsets[i], offsets[j]] = [offsets[j], offsets[i]];
  }
  return offsets.slice(0, count);
}
