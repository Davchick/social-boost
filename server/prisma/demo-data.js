/** Детерминированный «случайный» выбор — одинаковые данные при каждом запуске. */
export function pick(list, index) {
  return list[index % list.length];
}

/** Дата N дней назад (фиксированное время для стабильности). */
export function daysAgo(days, slot = 0) {
  const d = new Date();
  d.setHours(9 + (slot % 9), (slot * 13) % 60, 0, 0);
  d.setDate(d.getDate() - days);
  return d;
}

